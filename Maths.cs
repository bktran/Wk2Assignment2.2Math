using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Wk2Assignment2._2Math
{
    internal class Maths
    {
        public static int Add(int n1, int n2) 
        {
            return n1 + n2;
        }

        public static decimal Add(decimal n1, decimal n2, decimal n3) 
        {
            return n1 + n2 + n3;
        }

        public static float Multiply(float n1, float n2) 
        { 
            return n1 * n2; 
        }

        public static float Multiply(float n1, float n2, float n3)
        {
            return n1 * n2 * n3;
        }
    }
}
